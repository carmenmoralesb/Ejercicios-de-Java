    private List eliminaduplis (List<Integer> lista) {
       List <Integer> listaDuplicad = new ArrayList<Integer>(); 
       for(int i=1;i<lista.size();i++) {
          if(lista.get(i-1).equals(lista.get(i))) {
            listaDuplicad.add(lista.get(i));
            lista.remove(i);
            i = i -1;
                    }
            }
       return listaDuplicad;
    }

